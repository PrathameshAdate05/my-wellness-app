class Hospital {
  final String hospitalId;
  final String name;
  final String email;
  final String address;
  final String city;
  final String state;
  final String pincode;
  final String country;
  final String contact;
  final String type;
  final String createdAt;
  final String updatedAt;

  Hospital({
